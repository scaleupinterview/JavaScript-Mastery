// single quotes
var singQuotedString = 'This is a string with single quotes';

// Double quotes
var doubleQuotedString = "This is a string with double quotes";

// Escaping single and double quotes
var singleQuotes = 'This is a single-quoted string with a\'single quote\' inside.';
var doubleQuotes = "This is a double-quoted sting with a \"double quote\" inside.";

console.log(singleQuotes);
console.log(doubleQuotes);

// Escaping Backslash

var path = "C:\\Users\\John\\Documents\\file.txt";
console.log(path);

//  Newline Character (`\n`)
var multiLine = "This is a\nmulti-line\nstring.";
console.log(multiLine);

// Tab Character (`\t`)
var indented = "This is an indented\tstring";
console.log(indented);

// Carriage Return (`\r`)
var carriageReturn = "Line 1\rLine 2";
console.log(carriageReturn);

// Unicode Escape Sequence
var heart = "\u2764";
console.log(heart);