// Add and assign (+=)

let num = 5;
num += 3; //This is equilent to num = num + 3;
console.log(num) // output: 8

// Subtract and assign (-=)
let total = 10;
total -= 4; // This is equilent to total = total - 4;
console.log(total) // output: 6

// Multiply and assign (*=)
let count = 3;
count *= 5; // This is equilent to count = count * 5;
console.log(count); // output: 15


// Divide and assign (/=)
let value = 20;
value /= 4; // This is equilent to value = value / 4
console.log(value); // output: 5

// Modulus and assign (%=)
let num2= 17;
num2 %= 5; // This is equilent to num2 = num2 % 5
console.log(num2); // output: 2